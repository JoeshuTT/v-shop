<template>
  <div class="container">
    <SpainList v-model="listLoading">
      <AddressList v-if="list.length" :list="list" />
      <van-empty v-else class="empty" :description="listEmptyText"> </van-empty>
    </SpainList>
    <div class="affix-bar">
      <div class="affix-bar-action">
        <van-button class="affix-bar-button" type="primary" round @click="onAdd">
          <van-icon name="plus" />
          新建收货地址
        </van-button>
      </div>
    </div>
  </div>
</template>

<script>
import API_USER from '@/apis/user';
import SpainList from '@/components/SpainList';
import AddressList from '@/components/AddressList';

export default {
  components: { SpainList, AddressList },

  data() {
    return {
      list: [],
      listLoading: true,
      listEmptyText: '暂无地址',
    };
  },
  created() {
    this.getList();
  },
  methods: {
    onSelected(index) {
      this.list.forEach((v, i) => {
        this.$set(this.list[i], 'selected', false);
      });

      this.$set(this.list[index], 'selected', true);
    },
    onAdd() {
      this.$router.push({ path: '/address/edit' });
    },
    // eslint-disable-next-line max-params
    formatAddress(provinceStr, cityStr, areaStr, address) {
      let str = provinceStr;

      cityStr.length > 1 && (str += cityStr);
      areaStr.length > 1 && (str += areaStr);
      address && (str += ` ${address}`);

      return str;
    },
    onItemClicked(id) {
      this.$router.push({
        path: '/address/edit',
        query: {
          id: id,
        },
      });
    },
    getList() {
      this.listLoading = true;

      API_USER.userShoppingAddressList()
        .then((res) => {
          this.list = res.data?.result ?? [];
        })
        .finally(() => {
          this.listLoading = false;
        });
    },
  },
};
</script>

