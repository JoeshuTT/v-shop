<template>
  <van-popup
    :value="value"
    round
    closeable
    position="bottom"
    class="rate-popup"
    @click-close-icon="onClose"
    @click-overlay="onClose"
  >
    <div class="rate-header">发表评价</div>
    <div class="rate-body">
      <van-cell title="请选择您的评分" :border="false"></van-cell>
      <div class="rate-box">
        <van-rate v-model="rateValue" :size="24" :gutter="8" />
      </div>
      <van-field
        v-model="rateRemark"
        :border="false"
        label="备注"
        type="textarea"
        placeholder="非常愉快的一次购物！"
        maxlength="200"
        rows="1"
        autosize
      />
    </div>
    <div class="rate-actions">
      <van-button type="primary" round block @click="onSubmit">提交评价</van-button>
    </div>
  </van-popup>
</template>

<script>
import API_ORDER from '@/apis/order';
import usePage from '@/mixins/usePage';
import { reputation2Rate } from '@/model/modules/good/reputation';

export default {
  mixins: [usePage],
  props: {
    value: Boolean,
    goods: Array,
    orderInfo: Object,
  },
  data() {
    return {
      rateValue: 5,
      rateRemark: '',
    };
  },
  methods: {
    onClose() {
      this.$emit('input', false);
    },
    onOpen() {
      this.$emit('input', true);
    },
    onSubmit() {
      const reputations = this.goods.map((item) => ({
        id: item.id,
        reputation: reputation2Rate(this.rateValue), // 0 差评 1 中评 2 好评
        remark: this.rateRemark, // 评价备注，限200字符
      }));

      const params = {
        token: this.token,
        orderId: this.orderInfo.id,
        reputations: reputations,
      };

      this.$toast.loading({
        overlay: true,
        message: '加载中...',
        duration: 0,
      });

      API_ORDER.orderReputation({ postJsonString: JSON.stringify(params) })
        .then(() => {
          this.$toast({ message: '评价成功!', duration: 1500 });
          this.onClose();
          this.$emit('success');
        })
        .catch((error) => {
          console.error(error);
        });
    },
  },
};
</script>

